#As variavéis básicas do Phyton:
Num = 4102224 #Variavál tipo número inteiro (1, 2, 3...).
Name = "TestName01" #Variavél tipo string (texto). Apenas é considerada se tiver entre aspas
PiValue = 3.14 #Variavél tipo número real (como números decimais).
HaveReadABookToday = False #Variavél tipo bool (verdadeiro ou falso).

#Variavéis são valores definidos pelo programador ou digitados pelo usuário que são usados para armazenar informações e dados importantes.
#As Variavéis só podem conter letras antes de declarar seu valor, uma variavél não funciona se for com números. Por exemplo: 3 = 3.

print("Valor pra testes: ", Num, type(Num))
print("Nome: ", Name, type(Name))
print("Valor aprox. de Pi: ", PiValue, type(PiValue))
print("Já leu um livro hoje: ", HaveReadABookToday, type(HaveReadABookToday))
